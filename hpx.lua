		if animate then
			animate.Disabled = true
		end
	
		carregarAnimacoes(humanoid)
		tocarAnim("paradoCeu")
	
		humanoid.PlatformStand = false
		humanoid.AutoRotate = false
		humanoid.WalkSpeed = 0
		humanoid.JumpPower = 0
	
		humanoid:ChangeState(Enum.HumanoidStateType.Freefall)
	
		if bodyGyro then
			bodyGyro:Destroy()
		end
	
		if bodyVelocity then
			bodyVelocity:Destroy()
		end
	
		bodyGyro = Instance.new("BodyGyro")
		bodyGyro.Name = "HPX_FlyGyro"
		bodyGyro.P = 999999
		bodyGyro.D = 80
		bodyGyro.MaxTorque = Vector3.new(
			9999999999,
			9999999999,
			9999999999
		)
		bodyGyro.CFrame = root.CFrame
		bodyGyro.Parent = root
	
		bodyVelocity = Instance.new("BodyVelocity")
		bodyVelocity.Name = "HPX_FlyVelocity"
		bodyVelocity.MaxForce = Vector3.new(
			9999999999,
			9999999999,
			9999999999
		)
		bodyVelocity.P = 999999
		bodyVelocity.Velocity = Vector3.zero
		bodyVelocity.Parent = root
	
		if flyConnection then
			flyConnection:Disconnect()
		end
	
		flyConnection = RunService.RenderStepped:Connect(function(dt)
			if not flyAtivo then return end
			if not character.Parent then return end
			if not root.Parent then return end
			if not bodyGyro or not bodyVelocity then return end
	
			local camera = workspace.CurrentCamera
			local move = Vector3.zero
	
			local segurandoCtrl =
				UserInputService:IsKeyDown(Enum.KeyCode.LeftControl)
				or UserInputService:IsKeyDown(Enum.KeyCode.RightControl)
	
			if segurandoCtrl then
				boostExtra = math.clamp(
					boostExtra + boostAceleracao * dt,
					0,
					boostMaxExtra
				)
			else
				boostExtra = math.clamp(
					boostExtra - boostVolta * dt,
					0,
					boostMaxExtra
				)
			end
	
			if UserInputService:IsKeyDown(Enum.KeyCode.W) then
				move += camera.CFrame.LookVector
			end
	
			if UserInputService:IsKeyDown(Enum.KeyCode.S) then
				move -= camera.CFrame.LookVector
			end
	
			if UserInputService:IsKeyDown(Enum.KeyCode.A) then
				move -= camera.CFrame.RightVector
			end
	
			if UserInputService:IsKeyDown(Enum.KeyCode.D) then
				move += camera.CFrame.RightVector
			end
	
			if UserInputService:IsKeyDown(Enum.KeyCode.Space) then
				move += Vector3.new(0,1,0)
			end
	
			if UserInputService:IsKeyDown(Enum.KeyCode.Q) then
				move -= Vector3.new(0,1,0)
			end
	
			root.AssemblyAngularVelocity = Vector3.zero
	
			local velocidadeAtual = flySpeed + boostExtra
	
			if move.Magnitude > 0 then
				move = move.Unit
	
				bodyVelocity.Velocity = move * velocidadeAtual
	
				tocarAnim("voando")
	
				local target = criarCFrameVoo(
					root.Position,
					move
				)
	
				bodyGyro.CFrame = target
			else
				bodyVelocity.Velocity = Vector3.zero
	
				tocarAnim("paradoCeu")
	
				local target = criarCFrameParado(root.Position)
	
				bodyGyro.CFrame = target
			end
	
			atualizarBarra()
		end)
	
		log("========== FLY LIGADO ==========")
	end
	
	local function desligarFly()
		log("========== DESLIGANDO FLY ==========")
	
		if flyConnection then
			flyConnection:Disconnect()
			flyConnection = nil
		end
	
		if bodyGyro then
			bodyGyro:Destroy()
			bodyGyro = nil
		end
	
		if bodyVelocity then
			bodyVelocity:Destroy()
			bodyVelocity = nil
		end
	
		boostExtra = 0
	
		pararTodasAnimacoes()
	
		animacoesAntigas = {}
	
		ligarSomDePassos()
		restaurarColisao()
	
		local character = player.Character
	
		if character then
			local humanoid = character:FindFirstChildOfClass("Humanoid")
			local root = character:FindFirstChild("HumanoidRootPart")
	
			if humanoid then
				humanoid.AutoRotate = true
				humanoid.WalkSpeed = walkSpeedOriginal
				humanoid.JumpPower = jumpPowerOriginal
	
				humanoid:ChangeState(
					Enum.HumanoidStateType.GettingUp
				)
			end
	
			if root then
				root.AssemblyLinearVelocity = Vector3.zero
				root.AssemblyAngularVelocity = Vector3.zero
			end
	
			local animate = character:FindFirstChild("Animate")
	
			if animate then
				animate.Disabled = false
			end
		end
	
		atualizarBarra()
	
		log("========== FLY DESLIGADO ==========")
	end
	
